/**
 * Starts the premium extension installation flow.
 *
 * @return void
 */
function start_implecode_install() {
	$request_slug        = '';
	$request_license_key = '';
	$request_url         = '';
	$request_nonce       = '';
	// phpcs:ignore WordPress.Security.NonceVerification.Recommended -- Read-only install request routing.
	if ( isset( $_GET['slug'] ) ) {
		// phpcs:ignore WordPress.Security.NonceVerification.Recommended -- Read-only install request routing.
		$request_slug = sanitize_text_field( wp_unslash( $_GET['slug'] ) );
	}
	// phpcs:ignore WordPress.Security.NonceVerification.Recommended -- Read-only install request routing.
	if ( isset( $_GET['license_key'] ) ) {
		// phpcs:ignore WordPress.Security.NonceVerification.Recommended -- Read-only install request routing.
		$request_license_key = sanitize_text_field( wp_unslash( $_GET['license_key'] ) );
	}
	// phpcs:ignore WordPress.Security.NonceVerification.Recommended -- Read-only install request routing.
	if ( isset( $_GET['url'] ) ) {
		// phpcs:ignore WordPress.Security.NonceVerification.Recommended -- Read-only install request routing.
		$request_url = sanitize_text_field( wp_unslash( $_GET['url'] ) );
	}
	// phpcs:ignore WordPress.Security.NonceVerification.Recommended -- Read-only install request routing.
	if ( isset( $_GET['_wpnonce'] ) ) {
		// phpcs:ignore WordPress.Security.NonceVerification.Recommended -- Read-only install request routing.
		$request_nonce = sanitize_text_field( wp_unslash( $_GET['_wpnonce'] ) );
	}
	$upgrade_url = 'https://implecode.com/wordpress/plugins/' . rawurlencode( $request_url ) . '/#cam=extensions&key=' . rawurlencode( $request_url );
	// phpcs:ignore WordPress.Security.NonceVerification.Recommended -- Read-only install request routing.
	if ( isset( $_GET['implecode_install'] ) && ! empty( $request_slug ) && ! empty( $request_license_key ) && current_user_can( 'install_plugins' ) ) {
		if ( ! wp_verify_nonce( $request_nonce, 'install-implecode-plugin_' . $request_slug ) ) {
			return;
		}
		$api = implecode_installation_url();
		if ( is_wp_error( $api ) ) {
			$error_messages = $api->get_error_messages();
			$wp_errors      = implode( "\n", $error_messages );
			echo '<div id="message error" class="error product-adder-message messages-connect"><div class="squeezer"><h4><strong>' . esc_html__( 'There is a problem with the connection. Please contact your server administrator with the following error:', 'ecommerce-product-catalog' ) . '</strong></h4><p>' . esc_html( $wp_errors ) . '</p></div></div>';
		} elseif ( 'error' !== $api ) {
			add_filter( 'install_plugin_complete_actions', 'implecode_install_actions', 10, 3 );
			echo '<div class="extension_installer">';
			include_once ABSPATH . 'wp-admin/includes/class-wp-upgrader.php';
			$upgrader = new Plugin_Upgrader( new Plugin_Installer_Skin( array( 'api' => $api ) ) );
			$upgrader->install( $api->download_url );
			echo '</div>';
		} elseif ( ! is_license_key_prevalidated( $request_license_key ) ) {
			$message = sprintf(
				/* translators: %s: extension purchase URL. */
				__( 'This is not a valid license key! Get it <a href="%s">here</a>.', 'ecommerce-product-catalog' ),
				esc_url( $upgrade_url )
			);
			// phpcs:ignore WordPress.Security.EscapeOutput.OutputNotEscaped -- Message markup is escaped during assembly.
			echo '<div id="message error" class="error product-adder-message messages-connect"><div class="squeezer"><h4><strong>' . $message . '</strong></h4></div></div>';
		} else {
			$message = sprintf(
				/* translators: %s: extension upgrade URL. */
				__( 'The supplied license key is not valid for this extension! Upgrade it <a href="%s">here</a>.', 'ecommerce-product-catalog' ),
				esc_url( $upgrade_url )
			);
			// phpcs:ignore WordPress.Security.EscapeOutput.OutputNotEscaped -- Message markup is escaped during assembly.
			echo '<div id="message error" class="error product-adder-message messages-connect"><div class="squeezer"><h4><strong>' . $message . '</strong></h4></div></div>';
		}
	// phpcs:ignore WordPress.Security.NonceVerification.Recommended -- Read-only install request routing.
	} elseif ( isset( $_GET['implecode_install'] ) && ! empty( $request_slug ) && empty( $request_license_key ) && current_user_can( 'install_plugins' ) ) {
		$message = sprintf(
			/* translators: %s: extension purchase URL. */
			__( 'You need to provide the license key to activate the extension. Get yours <a href="%s">here</a>.', 'ecommerce-product-catalog' ),
			esc_url( $upgrade_url )
		);
		// phpcs:ignore WordPress.Security.EscapeOutput.OutputNotEscaped -- Message markup is escaped during assembly.
		echo '<div id="message error" class="error product-adder-message messages-connect"><div class="squeezer"><h4><strong>' . $message . '</strong></h4></div></div>';
	} elseif ( ! current_user_can( 'install_plugins' ) ) {
		echo '<div id="message error" class="error product-adder-message messages-connect"><div class="squeezer"><h4><strong>' . esc_html__( 'You don\'t have permission to install and activate extensions.', 'ecommerce-product-catalog' ) . '</strong></h4></div></div>';
	}
}


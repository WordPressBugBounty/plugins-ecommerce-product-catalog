/**
 * Returns active free impleCode plugins.
 *
 * @return array
 */
function get_implecode_active_free_plugins() {
	$all_active = get_option( 'active_plugins' );
	if ( ! function_exists( 'get_plugins' ) ) {
		require_once ABSPATH . 'wp-admin/includes/plugin.php';
	}
	$all_plugins = get_plugins();
	if ( empty( $all_plugins ) ) {
		return array();
	}
	$i          = 0;
	$ic_plugins = array();
	foreach ( $all_active as $active_name ) {
		if ( ! isset( $all_plugins[ $active_name ] ) ) {
			continue;
		}
		if ( 'impleCode' === $all_plugins[ $active_name ]['Author'] && 'eCommerce Product Catalog for WordPress' !== $all_plugins[ $active_name ]['Name'] && 'Product Catalog Simple' !== $all_plugins[ $active_name ]['Name'] ) {
			$ic_plugins[ $i ]['dir_file'] = $active_name;
			$active_name                  = explode( '/', $active_name );
			$ic_plugins[ $i ]['slug']     = $active_name[0];
		}
		++$i;
	}

	return $ic_plugins;
}

/**
 * Returns premium extension installation metadata.
 *
 * @return object|string|WP_Error
 */
function implecode_installation_url() {
	// phpcs:ignore WordPress.Security.NonceVerification.Recommended -- Nonce is verified before calling this helper.
	$license_key = isset( $_GET['license_key'] ) ? sanitize_text_field( wp_unslash( $_GET['license_key'] ) ) : '';
	// phpcs:ignore WordPress.Security.NonceVerification.Recommended -- Nonce is verified before calling this helper.
	$slug = isset( $_GET['slug'] ) ? sanitize_key( wp_unslash( $_GET['slug'] ) ) : '';
	if ( is_license_key_prevalidated( $license_key ) ) {
		$options = array(
			'timeout' => 10, // Seconds.
		);
		$url     = 'https://implecode.com/?action=get_metadata&slug=' . $slug . '&license_key=' . $license_key;
		$connect = wp_remote_get(
			$url,
			$options
		);
		if ( ! is_wp_error( $connect ) && ! empty( $connect['body'] ) ) {
			$plugin_info = json_decode( $connect['body'] );
			if ( isset( $plugin_info->download_url ) && '' !== $plugin_info->download_url ) {
				update_option( 'custom_license_code', $license_key, false );
